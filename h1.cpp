#include <bits/stdc++.h>
using namespace std;
int main() {
    int a, b;
    cout << "please input int 1:" << endl;
    cin >> a;
    cout << "please input int 2:" << endl;
    cin >> b;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << a << " % " << b << " = " << a % b << endl;
    return 0;
}